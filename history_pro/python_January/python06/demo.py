# isalnum()
# 功能：检测字符串是否由数字，字母和文字等组成
# 格式：字符串.isalnum()
# 返回值：布尔值  汉字当作普通字符处理，没有标点和特殊字符就是真，空字符串为false
#
# isalpha()
# 功能：检测字符串是否有字母和文字组成
# 格式：字符串.isalpha()
# 返回值：布尔值，汉字当作普通字母处理。空字符串为false
#
# isdigit()
# 功能：检测字符串是否由纯数字组成  十进制
# 格式：字符串.isdigit()
# 返回值：布尔值

# isnumeric()
# 功能：检测字符串是否是数值字符串  数字整数
# 格式:字符串.isnumeric()
# 返回值：布尔值
#
# isdecimal()
# 功能：检测字符串是否是纯数值字符串组成
# 格式:字符串.isdecimal()
# 返回值：布尔值
#
# isspace()
# 功能：检测字符串是否由空白字符组成
# 格式:字符串.isspace()
# 返回值：布尔值
#
# istitle()
# 功能：检测字符串是否符合title()的结果
# 格式：字符串.istitle()
# 返回值:布尔值
#
